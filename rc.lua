-- Function to fetch the volume and mute status using pactl
local function get_volume()
    awful.spawn.easy_async_with_shell("pactl get-sink-mute @DEFAULT_SINK@", function(mute_stdout)
        local is_muted = string.match(mute_stdout, "yes")
        awful.spawn.easy_async_with_shell("pactl get-sink-volume @DEFAULT_SINK@", function(stdout)
            local volume = string.match(stdout, "(%d?%d?%d)%%")
            volume = tonumber(volume)
            if volume == nil then
                volume = "N/A"
            else
                if is_muted then
                    volume = "Muted"
                else
                    volume = "VOL: " .. volume .. "%"
                end
            end
            volume_widget:set_text(volume)
        end)
    end)
end

-- Function to set the volume with a limit
local function set_volume(change)
    awful.spawn.easy_async_with_shell("pactl get-sink-volume @DEFAULT_SINK@", function(stdout)
        local current_volume = tonumber(string.match(stdout, "(%d?%d?%d)%%"))
        local new_volume = current_volume + change
        if new_volume > 150 then
            new_volume = 150
        elseif new_volume < 0 then
            new_volume = 0
        end
        awful.spawn.easy_async_with_shell("pactl set-sink-volume @DEFAULT_SINK@ " .. new_volume .. "%", function()
            get_volume()
        end)
    end)
end

-- Function to increase the volume
local function increase_volume()
    set_volume(5)
end

-- Function to decrease the volume
local function decrease_volume()
    set_volume(-5)
end

-- Function to toggle mute
local function toggle_mute()
    awful.spawn.easy_async_with_shell("pactl set-sink-mute @DEFAULT_SINK@ toggle", function()
        get_volume()
    end)
end

