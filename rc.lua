    local ssid = "Not connected"
    awful.spawn.easy_async_with_shell("iwctl station wlan0 show | grep 'Connected network' | awk '{print $3}'", function(stdout)
        if stdout and stdout ~= "" then
            ssid = " WLAN: " .. stdout:gsub("\n", "")
